/*問13　表｢商品｣から、「商品名｣の3文字目が｢D｣のデータを検索し、「商品番号｣と｢商品名｣を表示する*/

SELECT 商品番号, 商品名 FROM 商品 WHERE SUBSTRING(商品名, 3, 1) = 'D';


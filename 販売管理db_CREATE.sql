DROP DATABASE IF EXISTS 販売管理db;

CREATE DATABASE 販売管理db;

USE 販売管理db;

CREATE TABLE 受注(
受注番号   CHAR(10) PRIMARY KEY,
受注年月日 DATE,
顧客番号   VARCHAR(10),
受注合計   INT
);

CREATE TABLE 顧客(
顧客番号 CHAR(10) PRIMARY KEY,
顧客名   VARCHAR(30),
住所     VARCHAR(50),
電話番号 VARCHAR(15)
);

CREATE TABLE 受注明細(
受注番号 CHAR(10),
商品番号 CHAR( 5),
数量     INT,
受注小計 INT,
PRIMARY KEY(受注番号, 商品番号)
);

CREATE TABLE 商品(
商品番号 CHAR(5)PRIMARY KEY,
商品名   VARCHAR(30),
単価     INT
);


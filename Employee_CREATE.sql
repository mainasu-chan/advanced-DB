DROP DATABASE IF EXISTS Employee;

CREATE DATABASE Employee;

USE Employee;

/* 社員表 */
CREATE TABLE employee(
e_no CHAR(5) PRIMARY KEY,
e_name VARCHAR(20),
d_code CHAR(2),
t_code CHAR(1),
salary INT
);

/* 部署表 */
CREATE TABLE dept(
d_code CHAR(2) PRIMARY KEY,
d_name CHAR(10)
);

/* 役職表 */
CREATE TABLE title(
t_code CHAR(1) PRIMARY KEY,
t_name CHAR(10),
t_allowance INT
);

/* 資格表 */
CREATE TABLE cert(
c_code CHAR(3) PRIMARY KEY,
c_name VARCHAR(20),
c_allowance INT
);

/* 資格取得表 */
CREATE TABLE get_cert(
e_no CHAR(5),
c_code CHAR(3),
PRIMARY KEY(e_no, c_code)
);


